month = input('Enter the month:')

if month == 'june' or 'february' or 'march' or 'april' or 'may':
    print('spring')

elif month == 'june' or 'july' or 'august':
    print('summer')

elif month == 'september' or 'october' or 'november':
    print('fall')

elif month == 'december':
    print('Winter')

else:
    print('invalid month65')